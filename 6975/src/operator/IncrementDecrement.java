package operator;

public class IncrementDecrement {

	public static void main(String[] args) {
		int a=40;
		int b=50;
		System.out.println("The value of a "+a);
		System.out.println("The value of b "+b);
		System.out.println("The value of --a "+(--a));
		System.out.println("The value of a "+a);
		System.out.println("The value of b++ "+(b++));
		System.out.println("The value of b "+b);

	}

}
