package operator;

public class BitwiseOperator {

	public static void main(String[] args) {
		int a=2,b=4; // 0010 0100
		System.out.println("a&b= "+(a&b)); 
		System.out.println("a|b= "+(a|b)); 
		System.out.println("a^b= "+(a^b)); 
		System.out.println("~a= "+(~a));
		System.out.println("~b= "+(~b));
	}

}
