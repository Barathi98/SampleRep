package operator;

public class BitwiseShift {

	public static void main(String[] args) {
		int a=20;
		int b=-20;
		System.out.println("a divided by 2 is "+(a>>1));
		System.out.println("b divided by 2 is "+(b>>1));
		System.out.println("a multiply by 2 is "+(a<<1));
		System.out.println("b multiply by 2 is "+(b<<1));
		System.out.println("Unsigned right of a is "+(a>>>1));
		System.out.println("Unsigned right of b is "+(b>>>1));

	}

}
