package array;

public class Arrayprogram {

	public static void main(String[] args) {
		int s[]= {1,2,3,4,5};
		String name[]= {"barathi","dhiya","keerthi","manju","chandra"};
		int length=s.length;
		for(int i=0;i<=length-1;i++)
		{
			System.out.println(s[i]+" "+ name[i]);
		}

	}

}
